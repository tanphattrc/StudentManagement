    private void jtf_diemchuandatmonKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtf_diemchuandatmonKeyTyped
        // TODO add your handling code here:
        char  c =evt.getKeyChar();
        if(!(Character.isDigit(c)  || (c== KeyEvent.VK_BACK_SPACE) || c== KeyEvent.VK_DELETE ))
            evt.consume();
       if (jtf_diemchuandatmon.getText().length()==1)
       {
    	   if (Double.parseDouble(jtf_diemchuandatmon.getText()+c) > 10)
    	   {
    		   evt.consume();
    	   }
       }
        if (jtf_diemchuandatmon.getText().length() >= 2 ) // limit textfield to 3 characters
            evt.consume(); 
    }//GEN-LAST:event_jtf_diemchuandatmonKeyTyped

