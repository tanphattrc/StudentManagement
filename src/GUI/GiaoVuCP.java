    void LoadData() {
       
        List<GiaoVu> listGV;
        String[] columns = new String [] {
            "STT",
            "Điểm Chuẩn Đạt Môn",
            "Tuổi Học Sinh Tối Đa",  
            "Tuổi Học Sinh Tối Thiểu"
        };
        model = new DefaultTableModel(null , columns);
        
        listGV = GiaoVuDAO.layDanhSachGiaoVu();
        
        for (int i = 0; i < listGV.size(); i++) {
            GiaoVu gv = (GiaoVu) listGV.get(i);
            int stt = i;
            Object[] items = new Object[] {
                ++stt,
                gv.getdiemchuandatmon(),
                gv.gettuoitoida(),
                gv.gettuoitoithieu()
            };
            model.addRow(items);  
        }
        jtb_giaovu.setModel(model);
        jtb_giaovu.setRowHeight(30);
        jtb_giaovu.getColumnModel().getColumn(0).setPreferredWidth(50);
        jtb_giaovu.getColumnModel().getColumn(1).setPreferredWidth(150);
        jtb_giaovu.getColumnModel().getColumn(2).setPreferredWidth(150);
        jtb_giaovu.getColumnModel().getColumn(3).setPreferredWidth(150);

    }

