    private void jbt_capnhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_capnhatActionPerformed
        // TODO add your handling code here:
        if(!jtf_diemchuandatmon.getText().trim().equals("") && !jtf_tuoitoithieu.getText().trim().equals("") 
                && !jtf_tuoitoida.getText().trim().equals(""))
        {
            float diemchuandatmon = Float.parseFloat(jtf_diemchuandatmon.getText().trim()) ;
            int tuoitoithieu = Integer.parseInt(jtf_tuoitoithieu.getText().trim()) ;
            int tuoitoida =  Integer.parseInt(jtf_tuoitoida.getText().trim());
            GiaoVu gv = new GiaoVu(diemchuandatmon, tuoitoida,tuoitoithieu);
            boolean result = GiaoVuDAO.capNhatGiaoVu(gv);
            if (result) {           
                LoadData();
    //            Moi();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        }else
             JOptionPane.showMessageDialog(this, "Bạn cần nhập đúng dữ liệu", "Thông báo", JOptionPane.WARNING_MESSAGE);
        
        
    }//GEN-LAST:event_jbt_capnhatActionPerformed

